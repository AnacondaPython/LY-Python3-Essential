def main():
    # v is one
    v = 'one'
    if v == 'one':
        print('v is one')
    elif v == 'two':
        print('v is two')
    elif v == 'three':
        print('v is three')
    else: 
        print('v is some other thing ')

if __name__ == "__main__": main()
